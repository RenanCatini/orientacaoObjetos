package aula;	//Sempre a primeira opcao é package ou import

class helloWorld_eclipse {
	
	//Primeiro metodo sempre vai se chamar main
	public static void main(String[] args) {
		System.out.println("Hello Cruel Eclipse World!");
		System.out.println(args.length);
	}

}

/*
 * Classe:
 * Objeto:
 * 
 * Classe é um substantivo, nenhuma classe é verbo
 * 
 * psvn + tab : gera a estrutura basica Main do Java (intellj)
 * main + ctrl + space : gera a estrutura basica Main do Java (eclipse)
 * 
 * sysout + ctrl + space : System.out.println();
 * 
 * public : modificador de acesso que permite qualquer comando acessar
 * privado : somente dentro do metodo 
 * 
 * static : o metodo Main pertence apenas a classe helloWorld_eclipse.
 * 
 * (String[] args) : pode passar uma string como parametro
 */
